class TestAlreadySetReturnsAlreadySet:
    def test_already_true_returns_already_set_and_does_not_modify_mtime(
        self, tmp_path: Path
    ) -> None:
        config_path = tmp_path / 'config.yaml'
        config_path.write_text(
            '# Hand-written user comment\n'
            'gateway:\n'
            '  platforms:\n'
            '    myah:\n'
            '      enabled: true\n'
        )
        original_mtime = config_path.stat().st_mtime_ns
        original_text = config_path.read_text()

        result = enable_myah_platform(config_path)

        assert result == 'already_set'
        # We optimize the already-set path to skip the rewrite entirely so
        # user-authored comments survive unchanged.
        assert config_path.stat().st_mtime_ns == original_mtime
        assert config_path.read_text() == original_text


