# ── defense-in-depth sentinel: PyYAML must stay lazy-loaded ───────────


def test_module_does_not_import_yaml_at_top_level() -> None:
    """Sentinel: `yaml` must be lazy-imported inside `enable_myah_platform`.

    PyYAML is ~50-100ms of cold-start latency; if it leaks to module top
    every `myah` CLI invocation pays for it. This test mirrors
    `test_token_gen.test_module_imports_only_lightweight_stdlib`.
    """
    from myah.lib.cli import config_merge

    source = Path(config_merge.__file__).read_text(encoding='utf-8')
    head = source.split('\ndef ', 1)[0]
    # 'import yaml' or 'from yaml import …' at module top is the failure mode.
    assert 'import yaml' not in head, 'config_merge top-level imports yaml'
    assert 'from yaml' not in head, 'config_merge top-level imports from yaml'
